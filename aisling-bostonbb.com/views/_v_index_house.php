<p>
A 19th century Victorian row-house with its original marble fittings, fireplaces and mouldings, 
Aisling Bed and Breakfast is located at 21 East Concord Street, in the South End.
</p>
<p>
Aisling Bed and Breakfast has 3 en-suite rooms for your comfort. 
Each room has its own private bathroom and TV (with cable and pay per view) 
as well as wireless internet access. The house has central air-conditioning.
